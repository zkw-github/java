package com.student.zhaokangwei30.d29;

/**
 * 学习数学类
 */

public class TestMath {
    public static void main(String[] args) {
        System.out.println("自然对数："+Math.E);//静态常量
        System.out.println("圆周率："+Math.PI);//圆周率
        System.out.println("最大值："+Math.max(12,-1));//最大值
        System.out.println("最小值："+Math.min(3.14,-2.5));//最大值
        System.out.println("绝对值："+Math.abs(-123));//绝对值
        System.out.println("次幂值："+Math.pow(2,3));//次幂值
        System.out.println("平方根："+Math.sqrt(16));//平方根
        System.out.println("立方根："+Math.cbrt(27));//立方根
        System.out.println("正弦值："+Math.sin(Math.toRadians(30)));//正弦值
        System.out.println("正弦值："+Math.sin(Math.toRadians(60)));//正弦值
        System.out.println("余弦值："+Math.cos(Math.toRadians(60)));//余弦值
        System.out.println("正切值："+Math.tan(Math.toRadians(150)));//正切值
        System.out.println("反正弦："+Math.asin(Math.toRadians(30)));//反正弦
        System.out.println("反余弦："+Math.acos(Math.toRadians(30)));//反余弦
        System.out.println("反正切："+Math.atan(Math.toRadians(60)));//反正切
        System.out.println("向上取整："+Math.ceil(0.9));//向上取整
        System.out.println("向下取整："+Math.floor(2.9));//向下取整
        System.out.println("rint "+Math.rint(2.5));
        System.out.println("rint "+Math.rint(2.6));
        System.out.println(Math.exp(2));


    }

}
